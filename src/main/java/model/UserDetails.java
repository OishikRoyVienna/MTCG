package model;

public class UserDetails {

	private String bio;
	private String Name;
	private String image;
	private int packagesHold;
	
	public String getBio() {
		return bio;
	}
	public void setBio(String bio) {
		this.bio = bio;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public int getPackagesHold() {
		return packagesHold;
	}
	public void setPackagesHold(int packagesHold) {
		this.packagesHold = packagesHold;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
}
