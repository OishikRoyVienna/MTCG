package model;

public class Battle {

	private int battle;
	private int round;
	private String player1;
	private String player2;
	private String player1Card;
	private String player2Card;
	private String log;
	private String winner;
	private String roundStatus;
	private String battleStatus;
	
	public int getBattle() {
		return battle;
	}
	public void setBattle(int battle) {
		this.battle = battle;
	}
	public int getRound() {
		return round;
	}
	public void setRound(int round) {
		this.round = round;
	}
	public String getPlayer1() {
		return player1;
	}
	public void setPlayer1(String player1) {
		this.player1 = player1;
	}
	public String getPlayer2() {
		return player2;
	}
	public void setPlayer2(String player2) {
		this.player2 = player2;
	}
	public String getPlayer1Card() {
		return player1Card;
	}
	public void setPlayer1Card(String player1Card) {
		this.player1Card = player1Card;
	}
	public String getPlayer2Card() {
		return player2Card;
	}
	public void setPlayer2Card(String player2Card) {
		this.player2Card = player2Card;
	}
	public String getLog() {
		return log;
	}
	public void setLog(String log) {
		this.log = log;
	}
	public String getWinner() {
		return winner;
	}
	public void setWinner(String winner) {
		this.winner = winner;
	}
	public String getRoundStatus() {
		return roundStatus;
	}
	public void setRoundStatus(String roundStatus) {
		this.roundStatus = roundStatus;
	}
	public String getBattleStatus() {
		return battleStatus;
	}
	public void setBattleStatus(String battleStatus) {
		this.battleStatus = battleStatus;
	}
	
	
}
